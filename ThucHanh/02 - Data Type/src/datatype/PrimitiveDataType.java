package datatype;

public class PrimitiveDataType {

	public static void main(String[] args) {
		//Kiểu dữ liệu nguyên thủy (Primitive Data Types)
		//1
		byte bytetype = 100;
		System.out.println(bytetype);
		//2
		short shorttype = 1000;
		System.out.println(shorttype);
		//3
		int inttype = 12332123;
		System.out.println(inttype);
		//4
		long longtype = 200000l;
		System.out.println(longtype);
		//5
		float floattype = 2.5f;
		System.out.println(floattype);
		//6
		double doubletype = 2.54d;
		double doubletype02 = 2.5;
		System.out.println("1: " + doubletype + " , 2: " + doubletype02);
		//7
		char chartype = 'a';
		System.out.println(chartype);
		//8
		boolean booleantype = true;
		System.out.println(booleantype);
	}

}
